using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace desafioClaseOperacion
{
    class Operacion
    {
     
        public static int suma(int n1,int n2)
        {
            int resultado;

            resultado = n1 + n2;

            return resultado;
        }
        
        public static int resta(int n1,int n2)
        {
            int resultado;

            resultado = n1 - n2;

            return resultado;
        }        
    }
}
